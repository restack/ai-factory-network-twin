def test_interface_names_map_to_srlinux_conventions() -> None:
    assert srlinux_interface_name("eth1") == "ethernet-1/1"
    assert srlinux_interface_name("eth12") == "ethernet-1/12"
    assert srlinux_interface_name("lo") == "system0"
    assert srlinux_containerlab_interface_name("eth3") == "e1-3"
    with pytest.raises(ValueError, match="no SR Linux mapping"):
        srlinux_interface_name("Ethernet1/1")
    with pytest.raises(ValueError, match="no SR Linux Containerlab mapping"):
        srlinux_containerlab_interface_name("lo")


def test_leaf_config_renders_bgp_policies_and_interfaces() -> None:
    fabric = fixture_to_fabric(load_fixture(SRL_FIXTURE))
    leaf = next(node for node in fabric.nodes if node.name == "leaf-a1")
    spine = next(node for node in fabric.nodes if node.name == "spine-a1")

    leaf_config = render_srlinux_config(fabric, leaf)
    spine_config = render_srlinux_config(fabric, spine)

    assert leaf_config.startswith("# Generated by aftwin. DO NOT EDIT.\n")
    assert "set / interface system0 subinterface 0 ipv4 address 10.255.0.101/32" in leaf_config
    assert "set / network-instance default interface ethernet-1/1.0" in leaf_config
    assert "set / network-instance default interface system0.0" in leaf_config
    assert "set / network-instance default protocols bgp autonomous-system 65101" in leaf_config
    assert "ebgp-default-policy import-reject-all false" in leaf_config
    assert "ebgp-default-policy export-reject-all false" in leaf_config
    assert "multipath max-paths-level-1 64" in leaf_config
    assert "neighbor 10.0.0.0 peer-as 65001" in leaf_config
    assert "neighbor 10.0.0.4 peer-as 65002" in leaf_config
    # Leaves export their loopback and attached host prefixes; spines export
    # only their loopback and re-advertise BGP routes.
    assert "prefix 10.0.1.0/31 mask-length-range exact" in leaf_config
    assert "prefix 10.255.0.101/32 mask-length-range exact" in leaf_config
    assert "prefix 10.0.1.0/31" not in spine_config
    assert "prefix 10.255.0.1/32 mask-length-range exact" in spine_config
    assert "statement 20 match protocol bgp" in spine_config
    # Fabric p2p /31 subnets must not be exported as originated prefixes.
    assert "prefix 10.0.0.0/31" not in leaf_config
    assert leaf_config.endswith("\n")


